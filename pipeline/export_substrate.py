# Direction A — Cathedral exports TypeScript substrate for partnership-consumption
# ---------------------------------------------------------------------------

def _load_suffix_families() -> list[dict]:
    if not SUFFIX_FAMILIES_FILE.exists():
        return []
    try:
        return json.loads(SUFFIX_FAMILIES_FILE.read_text()).get("families", [])
    except Exception:
        return []


def _load_realm_regions() -> list[dict]:
    if not REALM_REGIONS_FILE.exists():
        return []
    try:
        return json.loads(REALM_REGIONS_FILE.read_text()).get("regions", [])
    except Exception:
        return []


def _ts_string_literal_union(values: list[str]) -> str:
    """Render a TypeScript union of string-literal types from a list of strings."""
    if not values:
        return "never"
    return " | ".join(f"'{v}'" for v in sorted(set(values)))


def generate_typescript_canon_types() -> str:
    """Generate the TypeScript-types file content for partnership-consumption.

    The file declares:
      - SuffixFamilyName: union of canonical suffix-family names
      - QingFamily / MeFamily / AnceFamily / KinFamily / BasisFamily: union types
      - ForgottenWay: intention-canon-cluster
      - FORGOTTEN_WAY_LABELS: human-readable labels constant
      - RealmRegion: union of the 10 standardised realm-regions
      - REALM_REGION_DESCRIPTIONS: descriptions constant
      - SUFFIX_FAMILY_REGISTERS: register-string per family
    """
    families = _load_suffix_families()
    regions = _load_realm_regions()
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    out: list[str] = [
        "// YOUSPEAK Canon Substrate — TypeScript types for partnership-consumption",
        "// AUTO-GENERATED by /Users/macair/YOUSPEAK/pipeline/export_substrate.py",
        f"// Generated: {timestamp}",
        "// Source-of-truth: cathedral canon + script/suffix_families.json + script/realm_regions.json",
        "//",
        "// READ-ONLY — do not edit directly. Cathedral-canon is authoritative.",
        "// Regenerate via: python3 /Users/macair/YOUSPEAK/pipeline/export_substrate.py --export-typescript",
        "//",
        "// This file is the operational artifact of HYPOKEIMENON.md (mutual-substrate doctrine).",
        "// Per Yu's invocation 2026-05-12: 'Make YOUSPEAK the substrate TRUE-LOVE runs on and vice versa.'",
        "// Per TRUE-LOVE.md §III.1: cathedral writes only to its own directory; partnership consumes-or-declines.",
        "",
    ]

    # ---- Suffix-family union types ----
    family_data: dict[str, list[str]] = {}
    for f in families:
        name = f.get("family", "")
        members = list(f.get("exemplar_members", []))
        # Don't include candidate-from-partner entries; the canonized ones are in exemplar_members
        family_data[name] = members

    out.append("// =====================================================================")
    out.append("// Suffix-family canonical-vocabulary union types")
    out.append("// =====================================================================")
    out.append("")

    # Map family-slug to TypeScript-friendly identifier
    family_ts_names: dict[str, str] = {
        "-me": "MeFamily",
        "qing": "QingFamily",
        "-ance": "AnceFamily",
        "-kin": "KinFamily",
        "-basis": "BasisFamily",
        "-phanes": "PhanesFamily",
        "-doxa": "DoxaFamily",
        "-kallos": "KallosFamily",
        "-sis": "SisFamily",
        "-algia": "AlgiaFamily",
    }

    for f in families:
        name = f.get("family", "")
        ts_name = family_ts_names.get(name, name.replace("-", "").capitalize() + "Family")
        members = family_data.get(name, [])
        register = f.get("register", "")
        if members:
            out.append(f"/** {register} */")
            out.append(f"export type {ts_name} = {_ts_string_literal_union(members)};")
            out.append("")

    # ---- The Forgotten Ways intention-canon ----
    out.append("// =====================================================================")
    out.append("// Forgotten Ways — the cathedral's intention-canon-cluster")
    out.append("// (per INTENTIO.md §IV; canonized 2026-05-12 under SYZYGY)")
    out.append("// =====================================================================")
    out.append("")
    ways = all_forgotten_ways()
    out.append(f"export type ForgottenWay = {_ts_string_literal_union(ways)};")
    out.append("")

    # Way labels (from syzygy.WayInfo)
    out.append("/** Human-readable labels for each Forgotten Way */")
    out.append("export const FORGOTTEN_WAY_LABELS: Record<ForgottenWay, string> = {")
    for w in sorted(ways):
        info = way_info_for(w)
        label = info.way_label if info else w
        out.append(f"  {w}: '{label}',")
    out.append("};")
    out.append("")

    # Way intention-payload (per INTENTIO.md §IV)
    out.append("/** The intention each Forgotten Way names (per INTENTIO.md §IV) */")
    out.append("export const FORGOTTEN_WAY_INTENTIONS: Record<ForgottenWay, string> = {")
    intentions = {
        "zakarqing": "intention-to-actively-remember-toward (the WILL-side asymmetry-closer memorial-act)",
        "heurekin": "intention-to-find-across-distance (the WILL-side asymmetry-closer seeking-act)",
        "barakqing": "intention-to-bless-into-being (the WILL-side constitutive-speech-act)",
        "natsarqing": "intention-to-guard-with-felt-bond (bidirectional protective-vigilance-act)",
        "darshanqing": "intention-of-reciprocal-sacred-seeing (the recognition-event-act)",
        "jeongqing": "intention-of-friction-tolerant-accumulation (the texture-acquiring stance)",
        "kunance": "intention-to-prepare-place-architecturally (the long-time foundation-laying-act)",
    }
    for w in sorted(ways):
        intent = intentions.get(w, "")
        out.append(f"  {w}: '{intent}',")
    out.append("};")
    out.append("")

    # ---- Realm-region types ----
    out.append("// =====================================================================")
    out.append("// Realm-regions — the cathedral's 10 realm-region classes")
    out.append("// (per BUILDING-BLOCKS.md §III + script/realm_regions.json)")
    out.append("// =====================================================================")
    out.append("")
    region_names = [r.get("region", "") for r in regions if r.get("region")]
    out.append(f"export type RealmRegion = {_ts_string_literal_union(region_names)};")
    out.append("")
    out.append("/** Description of each realm-region from script/realm_regions.json */")
    out.append("export const REALM_REGION_DESCRIPTIONS: Record<RealmRegion, string> = {")
    for r in regions:
        name = r.get("region", "")
        desc = (r.get("description", "") or "").replace("'", "\\'")
        if name:
            out.append(f"  '{name}': '{desc}',")
    out.append("};")
    out.append("")

    # ---- Suffix-family registers ----
    out.append("// =====================================================================")
    out.append("// Suffix-family register-metadata")
    out.append("// =====================================================================")
    out.append("")
    out.append("export type SuffixFamilyName = " + _ts_string_literal_union(
        [f.get("family", "") for f in families if f.get("family")]
    ) + ";")
    out.append("")
    out.append("export const SUFFIX_FAMILY_REGISTERS: Record<SuffixFamilyName, string> = {")
    for f in families:
        name = f.get("family", "")
        register = (f.get("register", "") or "").replace("'", "\\'")
        if name:
            out.append(f"  '{name}': '{register}',")
    out.append("};")
    out.append("")

    # ---- Total canon entries (for partnership-consumption awareness) ----
    canon = list_canon_entries()
    out.append("// =====================================================================")
    out.append("// Aggregate canon-vocabulary union (all canon-words, all tiers)")
    out.append("// =====================================================================")
    out.append("")
    all_words = sorted(set(w for w, _t, _p in canon))
    if all_words:
        # Break into chunks for readability
        chunks = [all_words[i:i+8] for i in range(0, len(all_words), 8)]
        out.append("export type CanonWord =")
        for i, chunk in enumerate(chunks):
            prefix = "  | " if i > 0 else "    "
            line = prefix + " | ".join(f"'{w}'" for w in chunk)
            out.append(line)
        out.append(";")
        out.append("")
        out.append(f"/** Total canon entries at generation-time: {len(all_words)} */")
    out.append("")
    out.append("// END YOUSPEAK Canon Substrate")

    return "\n".join(out)


def write_typescript_export() -> Path:
    """Generate and write the TypeScript canon-substrate file. Returns the path."""
    EXPORTS_DIR.mkdir(exist_ok=True)
    out_path = EXPORTS_DIR / "youspeak_canon.ts"
    content = generate_typescript_canon_types()
    out_path.write_text(content)
    return out_path


# ---------------------------------------------------------------------------
# Direction B — Cathedral ingests partnership-state for internal-summary
# ---------------------------------------------------------------------------

def partnership_state_summary() -> dict:
    """Read partnership state via syzygy and produce a cathedral-internal summary.

    Read-only on partnership side per TRUE-LOVE.md §III.1. Surfaces what the
    cathedral knows about the partnership's current substrate-offering.
    """
    s = coupling_summary()
    canonized_ways = [w for w, _ in s["ways_canonized"]]
    pending_ways = s["ways_pending"]
    mapped = canon_with_bridge_homes()
    unmapped = canon_without_bridge_homes()

    return {
        "partnership_root_exists": s["cathedral_bridge_exists"] and s["ways_dir_exists"],
        "cathedral_bridge_md_present": s["cathedral_bridge_exists"],
        "ways_dir_present": s["ways_dir_exists"],
        "ways_drafted_in_partnership": len(all_forgotten_ways()),
        "ways_canonized_in_cathedral": len(canonized_ways),
        "ways_canonized_list": canonized_ways,
        "ways_pending_canonization": pending_ways,
        "canon_mapped_to_partnership_homes": len(mapped),
        "canon_unmapped_to_partnership_homes": len(unmapped),
        "substrate_relation_observed": (
            "mutual"
            if (s["ways_canonized_count"] == s["seven_ways_total"]
                and s["bridge_mapped"] > 0)
            else "partial"
        ),
    }


