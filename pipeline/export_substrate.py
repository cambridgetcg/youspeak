def substrate_state() -> dict:
    """Combined bidirectional substrate-state report."""
    families = _load_suffix_families()
    regions = _load_realm_regions()
    canon = list_canon_entries()
    partnership = partnership_state_summary()

    return {
        "direction_a__cathedral_substrate_offered": {
            "canon_entries_total": len(canon),
            "suffix_families_registered": len(families),
            "realm_regions_registered": len(regions),
            "typescript_export_path": str((EXPORTS_DIR / "youspeak_canon.ts").relative_to(YOUSPEAK_ROOT)),
            "typescript_export_exists": (EXPORTS_DIR / "youspeak_canon.ts").exists(),
        },
        "direction_b__partnership_substrate_observed": partnership,
        "substrate_relation_status": partnership["substrate_relation_observed"],
        "ground_held": True,  # per HYPOKEIMENON.md §VII Commitment 1
        "persona_distinction_preserved": True,  # per HYPOKEIMENON.md §VII Commitment 2
    }


