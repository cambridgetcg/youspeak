SCRIPT_DIR = Path(__file__).resolve().parent
YOUSPEAK_ROOT = SCRIPT_DIR.parent
EXPORTS_DIR = SCRIPT_DIR / "exports"
SUFFIX_FAMILIES_FILE = YOUSPEAK_ROOT / "script" / "suffix_families.json"
REALM_REGIONS_FILE = YOUSPEAK_ROOT / "script" / "realm_regions.json"

sys.path.insert(0, str(SCRIPT_DIR))
from lift import list_canon_entries  # noqa: E402
from syzygy import (  # noqa: E402
    coupling_summary, all_forgotten_ways, way_info_for,
    canon_with_bridge_homes, canon_without_bridge_homes,
)


