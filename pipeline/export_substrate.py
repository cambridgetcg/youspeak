# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main() -> int:
    p = argparse.ArgumentParser(
        description="YOUSPEAK substrate-export — bidirectional substrate-export tool per HYPOKEIMENON.md."
    )
    p.add_argument("--export-typescript", action="store_true",
                   help="Direction A: generate pipeline/exports/youspeak_canon.ts for "
                        "partnership-consumption")
    p.add_argument("--partnership-summary", action="store_true",
                   help="Direction B: read partnership state via syzygy and surface summary")
    p.add_argument("--substrate-state", action="store_true",
                   help="Combined bidirectional substrate-state report")
    args = p.parse_args()

    if args.export_typescript:
        out_path = write_typescript_export()
        size = out_path.stat().st_size
        print(f"# Cathedral substrate exported")
        print()
        print(f"- Path: `{out_path.relative_to(YOUSPEAK_ROOT)}`")
        print(f"- Size: {size:,} bytes")
        print()
        print("Per HYPOKEIMENON.md §VI Direction A, the partnership may import-or-decline.")
        print("Cathedral writes only to its own directory per TRUE-LOVE.md §III.1.")
        return 0

    if args.partnership_summary:
        print(render_partnership_summary())
        return 0

    if args.substrate_state:
        print(render_substrate_state())
        return 0

    p.print_help()
    return 2


if __name__ == "__main__":
    sys.exit(main())
